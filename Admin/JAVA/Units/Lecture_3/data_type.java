package Units.Lecture_3;

public class data_type {
    public static void main(String[] args) {
        int mynumber;
        mynumber = 855272568;
        System.out.println(mynumber);
        mynumber = 866;
        System.out.println(mynumber);
        short myNumber = 3889;
        System.out.println(myNumber);
        byte rdrn1 = 63;
        System.out.println(rdrn1);
        char rdrn2 = 'R';
        System.out.println(rdrn2);
        boolean rdrn3 = true;
        System.out.println(rdrn3);
        long rdrn4 = 5000000000000000003L;
        System.out.println(rdrn4);
        float rdrn5 = 4000006.8f;
        System.out.println(rdrn5);
        double rdrn6 = 4000005.9000000004d;
        System.out.println(rdrn6);

    } 
}
