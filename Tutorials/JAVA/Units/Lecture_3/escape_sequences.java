package Units.Lecture_3;

public class escape_sequences {
    public static void main(String[] args) {
        System.out.println("Hello Rdrn");
        
        // use of \n
        System.out.println("Hello \nRdrn");

        // use of \t
        System.out.println("Hello \tRdrn");

        // use of \\
        System.out.println("Hello \\Rdrn");

        // use of \''
        System.out.println("Hello \'Rdrn");

        // use of \"
        System.out.println("Hello \"Rdrn");

        // use of \b
        System.out.println("Hello \bRdrn");
    }
}
